size_t		ft_words(char *str, char d)
{
	size_t i;
	size_t n;

	i = 0;
	n = 0;
	while (str[i] != '\0')
	{
		while (str[i] != d && str[i] != '\0')
			i++;
		n++;
		if (str[i] != '\0')
			i++;
	}
	return (n);
}

