int		ft_strspaces(char *s)
{
	int i;
	int j;

	j = 0;
	if (s != NULL)
	{
		i = 0;
		while (s[i] != '\0')
		{
			if (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
				j++;
			i++;
		}
	}
	return (j);
}
