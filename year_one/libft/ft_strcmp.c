#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	if (s1[0] == '\0' && s1[0] == s2[0])
		return (0);
	if (s1[0] == '\0' && s1[0] != s2[0])
		return (-1);
	if (s2[0] == '\0' && s2[0] != s1[0])
		return (1);
	return (ft_memcmp(s1, s2, ft_strlen(s1) + 1));
}
